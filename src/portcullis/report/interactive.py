_SCRIPT = """
(function () {
  var search = document.getElementById('search');
  var svc = document.getElementById('svc');
  var rule = document.getElementById('rule');
  var count = document.getElementById('count');
  var sevs = Array.prototype.slice.call(document.querySelectorAll('.sev'));
  var cards = Array.prototype.slice.call(document.querySelectorAll('.finding'));
  function apply() {
    var q = (search.value || '').toLowerCase();
    var checked = {};
    sevs.forEach(function (c) { checked[c.value] = c.checked; });
    var shown = 0;
    cards.forEach(function (card) {
      var ok = checked[card.getAttribute('data-severity')] !== false;
      if (ok && svc.value) ok = card.getAttribute('data-service') === svc.value;
      if (ok && rule.value) ok = card.getAttribute('data-rule') === rule.value;
      if (ok && q) ok = card.getAttribute('data-text').indexOf(q) !== -1;
      card.style.display = ok ? '' : 'none';
      if (ok) shown++;
    });
    if (count) count.textContent = 'showing ' + shown + ' of ' + cards.length;
  }
  [search, svc, rule].forEach(function (el) { if (el) { el.addEventListener('input', apply); } });
  sevs.forEach(function (c) { c.addEventListener('change', apply); });
  apply();
})();
"""
