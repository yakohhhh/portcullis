"""Self-contained HTML report.

The document is a single file with inline CSS and no external resources: no
CDN, no web fonts, no scripts, no network requests. This keeps the tool's
promise of 100% local operation - the report can be opened straight from disk
or attached to a CI job without leaking anything or phoning home. Reference
URLs are plain ``<a href>`` links, which are only fetched if the reader clicks
them.

Every dynamic value (service names, image references, finding prose that may
echo untrusted configuration) is HTML-escaped.
"""

from __future__ import annotations

from html import escape

from portcullis.model import Exposure, Finding, ScanResult, Severity

