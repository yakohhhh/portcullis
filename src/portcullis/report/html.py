def render_html(result: ScanResult, *, min_severity: Severity = Severity.INFO) -> str:
    findings = [f for f in result.findings if f.severity >= min_severity]
    parts: list[str] = []
    parts.append("<!DOCTYPE html>")
    parts.append('<html lang="en">')
    parts.append("<head>")
    parts.append('<meta charset="utf-8">')
    parts.append('<meta name="viewport" content="width=device-width, initial-scale=1">')
    parts.append("<title>Portcullis security report</title>")
    parts.append(f"<style>{_STYLE}</style>")
    parts.append("</head>")
    parts.append("<body><main>")
    parts.append(_render_header(result))
    parts.append(_render_exposure_table(result))
    parts.append(_render_findings(findings))
    parts.append(
        "<footer>Generated by Portcullis - 100% local, no data left your machine.</footer>"
    )
    parts.append("</main></body></html>")
    return "\n".join(parts) + "\n"


def _render_header(result: ScanResult) -> str:
    grade_color = GRADE_COLORS.get(result.grade, "#555")
    counts = _severity_counts(result.findings)
    chips = "".join(
        f'<span class="pill" style="background:{SEVERITY_COLORS[sev]}">'
        f"{escape(str(sev))}: {counts[sev]}</span> "
        for sev in sorted(Severity, reverse=True)
        if counts[sev]
    )
    return (
        "<h1>Portcullis security report</h1>"
        f'<p class="sub">Scanned path: <code>{escape(str(result.stack.root))}</code></p>'
        '<p class="summary-line">'
        f'<span class="grade" style="background:{grade_color}">{escape(result.grade)}</span> '
        f"&nbsp;score {result.score}/100 &middot; {len(result.stack.services)} services "
        f"&middot; {len(result.findings)} findings</p>"
        f'<p class="meta">{chips or "no findings"}</p>'
    )


def _render_exposure_table(result: ScanResult) -> str:
    rows: list[str] = []
    for name in sorted(result.stack.services):
        service = result.stack.services[name]
        image = service.image.raw if service.image else ("(build)" if service.build else "?")
        exposure = result.exposures.get(name, Exposure.UNKNOWN)
        color = EXPOSURE_COLORS[exposure]
        rows.append(
            "<tr>"
            f"<td><code>{escape(name)}</code></td>"
            f"<td><code>{escape(image)}</code></td>"
            f'<td><span class="pill" style="background:{color}">{escape(str(exposure))}</span></td>'
            "</tr>"
        )
    return (
        "<h2>Service exposure</h2>"
        "<table><thead><tr><th>Service</th><th>Image</th><th>Exposure</th></tr></thead>"
        f"<tbody>{''.join(rows)}</tbody></table>"
    )


def _render_findings(findings: list[Finding]) -> str:
    if not findings:
        return (
            '<h2>Findings</h2><p class="none">'
            "No findings at or above the requested severity.</p>"
        )
    blocks = [_render_finding(f) for f in findings]
    return "<h2>Findings</h2>" + "".join(blocks)


def _render_finding(finding: Finding) -> str:
    color = SEVERITY_COLORS[finding.severity]
    meta_bits = [f"<code>{escape(finding.rule_id)}</code>"]
    if finding.service:
        meta_bits.append(f"service <code>{escape(finding.service)}</code>")
    if finding.exposure is not None:
        meta_bits.append(f"exposure <strong>{escape(str(finding.exposure))}</strong>")
    if finding.source and finding.source != "portcullis":
        meta_bits.append(f"source <code>{escape(finding.source)}</code>")
    refs = ""
    if finding.references:
        links = " &middot; ".join(
            f'<a href="{escape(ref)}" rel="noreferrer noopener">{escape(ref)}</a>'
            for ref in finding.references
        )
        refs = f'<p class="refs">References: {links}</p>'
    return (
        f'<details style="border-left-color:{color}">'
        "<summary>"
        f'<span class="badge" style="background:{color}">{escape(str(finding.severity))}</span>'
        f'<span class="title">{escape(finding.title)}</span>'
        "</summary>"
        '<div class="body">'
        f'<p class="meta">{" &middot; ".join(meta_bits)}</p>'
        f"<p>{escape(finding.description)}</p>"
        f'<p><span class="label">Why it matters:</span> {escape(finding.risk)}</p>'
        f'<p class="fix"><span class="label">Fix:</span> {escape(finding.remediation)}</p>'
        f"{refs}"
        "</div></details>"
    )


def _severity_counts(findings: list[Finding]) -> dict[Severity, int]:
    counts = dict.fromkeys(Severity, 0)
    for finding in findings:
        counts[finding.severity] += 1
    return counts
